package com.class23;

public class Area {

	
	public void area (double length, double height) {
		System.out.println("The rectangle area equals to ");
		System.out.println(length*height);
	}
	
	public void area( int a) {
		System.out.println("The square area equals to ");
		System.out.println(a*a);
	}
	public void area ( double a) {
		System.out.println("The cube area equals to ");
		System.out.println(6*(a*a));
	}
}
